//propriedade lenght - Retorna o tamanho da string, iniciando em 0
document.write('Jorge Sant Ana'.length);
document.write('<br>');
//propriedade CharAt - Retorna o caractere no índice informado.
document.write('Jorge Sant Ana'.charAt(7));
document.write('<br>');
//propriedade IndexOf - Retorna a primeira posição em que o caractere informado é encontrado.
document.write('Jorge Sant Ana'.indexOf('g'));
document.write('<br>');
//propriedade Replace - Ele substitua o valor de pesquisa pelo informado.
document.write('Jorge Sant Ana'.replace('Sant Ana', 'Silva'));
document.write('<br>');
//propriedade Substr - Ele retorna o texto a partir de uma posição primária e pela quantidade de caracteres a ser retornado após a posição.
document.write('Jorge Sant Ana'.substr(6, 4));
document.write('<br>');
//propriedade TolowerCase
document.write('Jorge Sant Ana'.toLowerCase());
document.write('<br>');
//propriedade ToUpperCase
document.write('Jorge Sant Ana'.toUpperCase());
document.write('<br>');